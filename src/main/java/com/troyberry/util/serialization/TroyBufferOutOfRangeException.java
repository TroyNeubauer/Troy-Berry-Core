package com.troyberry.util.serialization;

public class TroyBufferOutOfRangeException extends TroyBufferException {

	public TroyBufferOutOfRangeException() {
		super();
	}

	public TroyBufferOutOfRangeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public TroyBufferOutOfRangeException(String message, Throwable cause) {
		super(message, cause);
	}

	public TroyBufferOutOfRangeException(String message) {
		super(message);
	}

	public TroyBufferOutOfRangeException(Throwable cause) {
		super(cause);
	}

}
