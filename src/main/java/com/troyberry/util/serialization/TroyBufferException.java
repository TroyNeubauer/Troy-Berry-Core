package com.troyberry.util.serialization;

public class TroyBufferException extends RuntimeException {

	public TroyBufferException() {
		super();
	}

	public TroyBufferException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public TroyBufferException(String message, Throwable cause) {
		super(message, cause);
	}

	public TroyBufferException(String message) {
		super(message);
	}

	public TroyBufferException(Throwable cause) {
		super(cause);
	}

}
